package com.mvcspringreactor.modelo;

public class FiguraEntity {
	private int id;
	private String tipo;
	
	public FiguraEntity() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	

}
